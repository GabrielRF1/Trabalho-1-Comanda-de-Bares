    private int ContarQntsPedidosTem(Comanda com) {
        int total = 0;
        for (Pedido pe : com.getPedidos()) {
            if (pe.getNome().equals(jComboBox2.getSelectedItem())) {
                total += pe.getQuantidade();
            }
        }
        return total;
    }

